print(abs(-10))  # valor absoluto
print(pow(2, 2))  # exponencial
print(max(-1, 2, 4, -18))  # valor máximo
print(min(-1, 2, 4, -18))  # valor mínimo
print(round(9.999999)) # arredondamento com base em aproximação

import math # importando uma biblioteca de matemática do python


print(math.floor(9.999999))  # arredondamento para baixo
print(math.ceil(9.999999))  # arredondamento para cima
print(math.sqrt(25))  # raiz quadrada em float